#include <iostream>
#include <iomanip>

using namespace std;

int main() {
    // задача Begin20
    // декларація змінних
    double alphaDegrees;      // кут у градусах
    double alphaRadians;      // кут у радіанах
    const double PI = 3.14;   // значення π за умовою

    // виведення запрошення до вводу
    cout << "Введіть кут у градусах (0 <= alpha < 360): ";
    if (!(cin >> alphaDegrees)) {
        // перевірка коректності вводу (не число)
        cerr << "Некоректний ввід: введіть дійсне число." << endl;
        return 1;
    }

    // перевірка діапазону
    if (alphaDegrees < 0.0 || alphaDegrees >= 360.0) {
        cerr << "Некоректний діапазон: alpha має задовольняти 0 <= alpha < 360." << endl;
        return 1;
    }

    // розрахунок у радіанах
    alphaRadians = alphaDegrees * PI / 180.0;

    // вивід результату
    cout << fixed << setprecision(6);
    cout << "Кут у радіанах: " << alphaRadians << endl;

    // задача Begin2
    // декларація змінних
    double a;   // сторона квадрата
    double S;   // площа квадрата

    // виведення запрошення до вводу
    cout << "Введіть сторону квадрата a (a > 0): ";
    if (!(cin >> a)) {
        // перевірка коректності вводу (не число)
        cerr << "Некоректний ввід: введіть дійсне число." << endl;
        return 1;
    }

    // перевірка позитивності a
    if (a <= 0.0) {
        cerr << "Некоректне значення: a має бути більше нуля." << endl;
        return 1;
    }

    // розрахунок площі
    S = a * a;

    // вивід результату
    cout << fixed << setprecision(6);
    cout << "Площа квадрата S = " << S << endl;

    // задача Begin50
    // декларація змінних
    double a, b, c;   // вхідні дійсні числа
    double M;         // середнє арифметичне
    double Q;         // сума квадратів

    // виведення запрошення до вводу
    cout << "Введіть три дійсних числа a b c (через пробіл): ";

    // зчитування і перевірка коректності вводу
    if (!(cin >> a >> b >> c)) {
        cerr << "Некоректний ввід: введіть три дійсні числа." << endl;
        return 1;
    }

    // розрахунок середнього арифметичного і суми квадратів
    M = (a + b + c) / 3.0;
    Q = a * a + b * b + c * c;

    // вивід результатів
    cout << fixed << setprecision(6);
    cout << "Середнє арифметичне M = " << M << endl;
    cout << "Сума квадратів Q = " << Q << endl;

    return 0;
}

